public class DokterData {
    private String gambarDokter;
    private String namaDokter;
    private String spesialisDokter;
    private String pengalamanDokter;

    public String getGambarDokter() {
        return gambarDokter;
    }

    public void setGambarDokter(String gambarDokter) {
        this.gambarDokter = gambarDokter;
    }

    public String getNamaDokter() {
        return namaDokter;
    }

    public void setNamaDokter(String namaDokter) {
        this.namaDokter = namaDokter;
    }

    public String getSpesialisDokter() {
        return spesialisDokter;
    }

    public void setSpesialisDokter(String spesialisDokter) {
        this.spesialisDokter = spesialisDokter;
    }

    public String getPengalamanDokter() {
        return pengalamanDokter;
    }

    public void setPengalamanDokter(String pengalamanDokter) {
        this.pengalamanDokter = pengalamanDokter;
    }
    
    
}
